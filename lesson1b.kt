// Here we specify the data types

fun main() {
    val myNum: Int = 10    //Int
    val myDoubleNum: Double = 5.99   //Double
    val myLetter: Char = 'A'  //Character
    val myBoolean: Boolean = true  //Boolean
    val myText: String = "Hello"   //String

    println(myNum)
    println(myDoubleNum)
    println(myLetter)
    println(myBoolean)
    println(myText)
    
}